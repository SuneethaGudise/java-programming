package com.mock;

public class RectangleArea
{
 private  int length;
 private int width;

  
  public RectangleArea(int length ,int width)
  {
	this.length =length;
	this.width =width;
  }
  
public int getLength()
  {
	  return length;
  }
  public int getWidth()
  {
	  return width;
  }
  public int Area()
  {
    int a= length*width;
    return a;
}
}
	
	
 
 
